# ============================================================================
# UI Spec Schema
# ============================================================================
# This is what the agent generates

class UIElement(BaseModel):
    """A single UI element in the component tree"""
    type: str  # Component name (Card, Stack, TextInput, Button, etc.)
    props: dict  # Component-specific properties
    children: list[str] = []  # IDs of child elements


class UISpec(BaseModel):
    """Complete UI specification generated by the agent"""
    root: str  # ID of the root element
    elements: dict[str, UIElement]  # Map of element IDs to definitions


# ============================================================================
# Tool Definitions for the Agent
# ============================================================================

def generate_ui_spec(description: str) -> UISpec:
    """Generate a UI specification from a natural language description.
    
    This tool would typically use the catalog to guide the AI in
    generating valid component combinations.
    
    Args:
        description: Natural language description of desired UI
    
    Returns:
        A valid UISpec that can be rendered by the frontend
    """
    # For demonstration, return a sample UI spec
    # In production, this would use structured output from the LLM
    return UISpec(
        root="main-card",
        elements={
            "main-card": UIElement(
                type="Card",
                props={"title": "Weather App", "padding": "md"},
                children=["content-stack"]
            ),
            "content-stack": UIElement(
                type="Stack",
                props={"direction": "vertical", "gap": "md"},
                children=["city-input", "search-btn", "result-area"]
            ),
            "city-input": UIElement(
                type="TextInput",
                props={
                    "label": "Enter city name",
                    "placeholder": "e.g., New York, London",
                    "type": "text"
                },
                children=[]
            ),
            "search-btn": UIElement(
                type="Button",
                props={"label": "Get Weather", "variant": "primary", "fullWidth": True},
                children=[]
            ),
            "result-area": UIElement(
                type="Card",
                props={"title": "Weather Information", "padding": "sm"},
                children=["result-text"]
            ),
            "result-text": UIElement(
                type="Text",
                props={"content": "Weather will appear here...", "size": "md"},
                children=[]
            )
        }
    )


def get_weather(city: str) -> str:
    """Get weather for a city (tool for agent)"""
    return f"The weather in {city} is sunny with a temperature of 72°F."


def search_web(query: str) -> str:
    """Search the web (tool for agent)"""
    return f"Search results for '{query}': ..."


