@app.get("/health")
def health_check():
    """Health check endpoint"""
    return {"status": "ok"}


@app.get("/catalog")
def get_catalog():
    """Get the component catalog available to the AI.
    
    The frontend can use this to understand what components
    the agent can generate.
    """
    return COMPONENT_CATALOG


@app.post("/generate-ui")
def generate_ui(request: dict):
    """Generate a UI specification from a description.
    
    Request body:
    {
        "description": "Create a login form"
    }
    
    Returns:
    A UISpec that the frontend can render
    """
    description = request.get("description", "")
    if not description:
        raise HTTPException(status_code=400, detail="Description required")
    
    # Generate UI spec using the agent
    # In a real implementation, this would use the LLM
    ui_spec = generate_ui_spec(description)
    
    return ui_spec.model_dump()


@app.post("/stream-ui")
def stream_ui(request: dict):
    """Stream UI specification generation (for real-time updates).
    
    This endpoint streams the UI spec as it's being generated,
    allowing the frontend to display UI components progressively.
    """
    description = request.get("description", "")
    if not description:
        raise HTTPException(status_code=400, detail="Description required")
    
    async def generate():
        """Generator that yields UI spec updates as JSON lines"""
        try:
            # Simulate progressive UI generation
            # In production, this would stream from the LLM
            
            # First, stream the root element
            yield 'data: {"event": "element_added", "element": {"id": "main-card", "type": "Card", "props": {"title": "Generated UI", "padding": "md"}}}\n\n'
            
            # Then stream child elements
            elements = [
                ('content-stack', 'Stack', '{"direction": "vertical", "gap": "md"}'),
                ('city-input', 'TextInput', '{"label": "Enter city", "placeholder": "City name"}'),
                ('search-btn', 'Button', '{"label": "Search", "variant": "primary"}'),
            ]
            
            for elem_id, elem_type, elem_props in elements:
                yield f'data: {{"event": "element_added", "element": {{"id": "{elem_id}", "type": "{elem_type}", "props": {elem_props}}}}}\n\n'
            
            # Finally, send the complete spec
            ui_spec = generate_ui_spec(description)
            yield f'data: {{"event": "complete", "spec": {json.dumps(ui_spec.model_dump())}}}\n\n'
            
        except Exception as e:
            yield f'data: {{"event": "error", "message": "{str(e)}"}}\n\n'
    
    return StreamingResponse(
        generate(),
        media_type="text/event-stream",
        headers={"Cache-Control": "no-cache"}
    )


@app.post("/agent/chat")
def agent_chat(request: dict):
    """Chat with the agent using natural language.
    
    The agent understands the UI component catalog and can
    generate appropriate UIs based on your description.
    
    Request body:
    {
        "message": "Create a form to collect user feedback"
    }
    
    Returns:
    {
        "response": "I've created a feedback form UI for you.",
        "ui_spec": { ... },
        "used_tools": ["generate_ui_spec"]
    }
    """
    message = request.get("message", "")
    if not message:
        raise HTTPException(status_code=400, detail="Message required")
    
    try:
        # Initialize the agent
        model = ChatOllama(model="qwen3.5:latest")
        
        # Define system prompt that includes catalog info
        system_prompt = f"""You are a helpful UI generation assistant.
        
You can generate UI specifications using these available components:
{json.dumps(COMPONENT_CATALOG['components'], indent=2)}

When asked to create a UI, use the generate_ui_spec tool.
Always consider the best components for the user's needs.

For example:
- For data display: use Table or Stack with Text
- For input: use TextInput, Button
- For containers: use Card, Stack
- For status: use Badge
"""
        
        agent = create_agent(
            model=model,
            tools=[generate_ui_spec, get_weather, search_web],
            system_prompt=system_prompt
        )
        
        # Invoke the agent
        result = agent.invoke({"messages": [{"role": "user", "content": message}]})
        
        # Extract the response
        last_message = result["messages"][-1]
        response_text = last_message.content if hasattr(last_message, 'content') else str(last_message)
        
        return {
            "response": response_text,
            "success": True,
            "message_count": len(result["messages"])
        }
    
    except Exception as e:
        return {
            "response": f"Error: {str(e)}",
            "success": False
        }


