"""
LangChain Agent Frontend Example: Generative UI Agent

This example demonstrates:
- Building an agent that generates UI specifications as JSON
- Using structured output to ensure valid component schemas
- Streaming UI generation for real-time updates
- Integration with React frontend via API

Generative UI Pattern:
1. Developer defines a component catalog (available components)
2. Agent generates JSON specs describing the UI tree
3. Frontend renders the specs using json-render or similar
4. Users see UI generated in real-time as the agent streams

This is a backend service that serves UI specs to a React frontend.
"""

